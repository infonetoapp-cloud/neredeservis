/* ─── Bearing between two LatLng points (degrees, 0=N, 90=E) ─────────────── */
function calcBearing(a: [number, number], b: [number, number]): number {
  const toRad = (d: number) => (d * Math.PI) / 180;
  const toDeg = (r: number) => (r * 180) / Math.PI;
  const dLng = toRad(b[1] - a[1]);
  const lat1 = toRad(a[0]);
  const lat2 = toRad(b[0]);
  const y = Math.sin(dLng) * Math.cos(lat2);
  const x =
    Math.cos(lat1) * Math.sin(lat2) -
    Math.sin(lat1) * Math.cos(lat2) * Math.cos(dLng);
  return (toDeg(Math.atan2(y, x)) + 360) % 360;
}

/* ─── Interpolate position along a polyline by fraction t ∈ [0,1] ─────── */
function interpRoute(
  route: [number, number][],
  t: number,
): { pos: [number, number]; bearing: number } {
  const n = route.length;
  if (n < 2) return { pos: route[0], bearing: 0 };

  // Build cumulative distance array (cheap euclidean — fine at city scale)
  const cumDist: number[] = [0];
  for (let i = 1; i < n; i++) {
    const dlat = route[i][0] - route[i - 1][0];
    const dlng = route[i][1] - route[i - 1][1];
    cumDist.push(cumDist[i - 1] + Math.sqrt(dlat * dlat + dlng * dlng));
  }
  const total = cumDist[n - 1];
  const target = Math.max(0, Math.min(1, t)) * total;

  // Binary-search for correct segment
  let lo = 0;
  let hi = n - 1;
  while (lo < hi - 1) {
    const mid = (lo + hi) >> 1;
    if (cumDist[mid] <= target) lo = mid;
    else hi = mid;
  }
  const seg = lo;
  const segLen = cumDist[seg + 1] - cumDist[seg];
  const frac = segLen > 0 ? (target - cumDist[seg]) / segLen : 0;

  const lat = route[seg][0] + frac * (route[seg + 1][0] - route[seg][0]);
  const lng = route[seg][1] + frac * (route[seg + 1][1] - route[seg][1]);
  const bearing = calcBearing(route[seg], route[seg + 1]);

  return { pos: [lat, lng], bearing };
}

