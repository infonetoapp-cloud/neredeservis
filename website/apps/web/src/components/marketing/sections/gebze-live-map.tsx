export function GebzeLiveMap() {
  const containerRef = useRef<HTMLDivElement>(null);
  const mapRef = useRef<import("leaflet").Map | null>(null);
  const animRef = useRef<number>(0);

  useEffect(() => {
    if (!containerRef.current) return;
    ensureLeafletCss();
    let cancelled = false;

    (async () => {
      const L = await import("leaflet");
      if (cancelled || !containerRef.current) return;

      /* ── Map setup ── */
      const map = L.map(containerRef.current, {
        center: GEBZE_CENTER,
        zoom: DEFAULT_ZOOM,
        zoomControl: true,
        attributionControl: true,
        dragging: true,
        scrollWheelZoom: false,   // prevent page-scroll hijack
        doubleClickZoom: true,
        touchZoom: true,
        boxZoom: false,
        keyboard: false,
      });
      mapRef.current = map;

      /* ── Tile layer (OpenStreetMap — free, no key) ── */
      L.tileLayer("https://tile.openstreetmap.org/{z}/{x}/{y}.png", {
        attribution:
          '&copy; <a href="https://www.openstreetmap.org/copyright">OpenStreetMap</a>',
        maxZoom: 18,
      }).addTo(map);

      /* ── Route polylines (dashed) ── */
      for (const v of VEHICLES) {
        L.polyline(v.route, {
          color: v.color,
          weight: 3,
          opacity: 0.4,
          dashArray: "8 6",
          lineCap: "round",
        }).addTo(map);
      }

      /* ── Vehicle markers ── */
      const markers = VEHICLES.map((v, idx) => {
        const init = interpRoute(v.route, 0);
        const marker = L.marker(init.pos, {
          icon: vehicleDivIcon(L, v.plate, v.color, init.bearing),
          interactive: false,
          zIndexOffset: 1000,
        }).addTo(map);
        return {
          marker,
          vehicle: v,
          lastBearing: init.bearing,
          // Stagger start so vehicles don't all begin at 0
          offset: idx * 0.18,
        };
      });

      /* ── Animation loop — vehicles ping-pong along their routes ── */
      const start = performance.now();

      function animate() {
        const elapsed = (performance.now() - start) / 1000;

        for (const m of markers) {
          // Each vehicle has its own cycle duration
          const cycleSec = m.vehicle.route.length / (m.vehicle.speed * 12);
          const raw = ((elapsed + m.offset * cycleSec) / cycleSec) % 2;
          const t = raw > 1 ? 2 - raw : raw; // ping-pong 0→1→0

          const { pos, bearing } = interpRoute(m.vehicle.route, t);

          // Smooth bearing to avoid jitter
          let dB = bearing - m.lastBearing;
          if (dB > 180) dB -= 360;
          if (dB < -180) dB += 360;
          const smooth = m.lastBearing + dB * 0.12;
          m.lastBearing = smooth;

          m.marker.setLatLng(pos);
          m.marker.setIcon(
            vehicleDivIcon(L, m.vehicle.plate, m.vehicle.color, smooth),
          );
        }
        animRef.current = requestAnimationFrame(animate);
      }

      animRef.current = requestAnimationFrame(animate);
    })();

    return () => {
      cancelled = true;
      cancelAnimationFrame(animRef.current);
      if (mapRef.current) {
        mapRef.current.remove();
        mapRef.current = null;
      }
    };
  }, []);

  return (
    <div
      ref={containerRef}
      className="h-[360px] w-full sm:h-[420px]"
      style={{ background: "#f0f9ff" }}
    />
  );
}
